SELECT DISTINCT
    c.customer_id,
    c.fio AS клиент,
    h.h_name AS отель, 
    h.h_city AS город
FROM booking b
JOIN customers c ON b.customers_customer_id = c.customer_id
JOIN rooms r ON b.rooms_room_id = r.room_id
JOIN hotels h ON r.hotels_hotel_id = h.hotel_id
ORDER BY c.customer_id;

SELECT 
    h.h_name AS отель,
    rt.type_name AS тип_номера,
    ROUND(AVG(r.price_per_night), 2) AS средняя_цена_за_ночь
FROM rooms r
JOIN hotels h ON r.hotels_hotel_id = h.hotel_id
JOIN room_type rt ON r.room_type_room_type_id = rt.room_type_id
GROUP BY h.h_name, rt.type_name
ORDER BY h.h_name, средняя_цена_за_ночь DESC;

SELECT 
    c.customer_id,
    c.fio AS клиент,
    b.check_in_date AS дата_заезда,
    b.check_out_date AS дата_выезда,
    COUNT(*) AS количество_броней
FROM booking b
JOIN customers c ON b.customers_customer_id = c.customer_id
GROUP BY c.customer_id, c.fio, b.check_in_date, b.check_out_date
HAVING COUNT(*) > 1
ORDER BY c.customer_id;